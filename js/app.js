function togglePopupLogin(){
    var popup = document.getElementById("loginpopup");
    if (popup.style.display === 'flex') {
        popup.style.display = 'none';
    }
    else {
        popup.style.display = 'flex';
    }
};

function closePopupLogin(){
    var popup = document.getElementById("loginpopup");
    if (popup.style.display === 'flex') {
        popup.style.display = 'none';
    }
    else {
        popup.style.display = 'flex';
    }
};